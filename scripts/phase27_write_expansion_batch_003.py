def _record(
    *,
    dialect: str,
    source: str,
    topic_index: int,
    frame_index: int,
    messages: list[dict[str, str]],
) -> dict[str, object]:
    return {
        "domain": "chat",
        "lang": "ar",
        "messages": messages,
        "provenance": {
            "source": source,
            "license": LICENSE,
            "language": "ar",
            "dialect": dialect,
            "quality": QUALITY,
            "training_allowed": True,
            "owner_user_id": USER_ID,
            "created_by_user_id": "agent-authorized-codex",
            "target_user_id": USER_ID,
            "user_scope": "single_user",
            "notes": (
                f"{BATCH_ID}; owner-delegated agent-authored; "
                f"topic={topic_index:02d}; frame={frame_index:02d}; "
                "no external corpus; no pretrained model output"
            ),
        },
    }


def _build_records(
    *,
    dialect: str,
    source: str,
    topics: tuple[tuple[str, str, str, str, str, str], ...],
    frames: tuple[tuple[str, str, str], ...],
) -> list[dict[str, object]]:
    records: list[dict[str, object]] = []
    seen: set[str] = set()
    for topic_index, (u1, a1, u2, a2, u3, a3) in enumerate(topics, start=1):
        for frame_index, (prefix, bridge, closer) in enumerate(frames, start=1):
            messages = [
                {"role": "user", "content": f"{prefix} {u1}"},
                {"role": "assistant", "content": f"{bridge} {a1}"},
                {"role": "user", "content": u2},
                {"role": "assistant", "content": a2},
                {"role": "user", "content": u3},
                {"role": "assistant", "content": f"{closer} {a3}"},
            ]
            key = "\n".join(msg["content"] for msg in messages)
            if key in seen:
                raise RuntimeError(f"duplicate generated record: {dialect} {topic_index}/{frame_index}")
            seen.add(key)
            records.append(
                _record(
                    dialect=dialect,
                    source=source,
                    topic_index=topic_index,
                    frame_index=frame_index,
                    messages=messages,
                )
            )
    if len(records) != 250:
        raise RuntimeError(f"expected 250 {dialect} records, got {len(records)}")
    return records


def _write_jsonl(path: Path, records: list[dict[str, object]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        for record in records:
            handle.write(json.dumps(record, ensure_ascii=False, separators=(",", ":")) + "\n")


def _write_card(path: Path, *, title: str, dialect: str, count: int) -> None:
    path.write_text(
        "\n".join(
            [
                f"# {title}",
                "",
                f"- records: {count}",
                f"- dialect: {dialect}",
                "- language: ar",
                f"- quality: {QUALITY}",
                f"- license: {LICENSE}",
                "- training_allowed: true",
                f"- owner_user_id: {USER_ID}",
                f"- target_user_id: {USER_ID}",
                "- source_type: owner-delegated agent-authored",
                "- external_sources: none",
                "- pretrained_model_outputs: none",
                "- purpose: Phase 27 large corpus expansion toward SF-50M readiness",
                "- notes: batch 003 keeps the approved 500-record large-batch size.",
                "",
            ]
        ),
        encoding="utf-8",
    )


