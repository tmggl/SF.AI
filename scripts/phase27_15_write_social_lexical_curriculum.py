def _record(dialect: str, user: str, assistant: str) -> dict[str, object]:
    return {
        "domain": "chat",
        "lang": "ar",
        "messages": [
            {"role": "user", "content": user},
            {"role": "assistant", "content": assistant},
        ],
        "provenance": {
            "source": f"sf-ai-social-lexical-curriculum-{dialect}-v1",
            "license": LICENSE,
            "language": "ar",
            "dialect": dialect,
            "quality": "gold",
            "training_allowed": True,
            "owner_user_id": OWNER_USER_ID,
            "created_by_user_id": CREATOR_USER_ID,
            "target_user_id": OWNER_USER_ID,
            "user_scope": "single_user",
            "notes": "everyday social and lexical repair; owner delegated local authoring; no external dataset",
        },
    }


def _build(curriculum: list[tuple[str, list[str]]], dialect: str) -> list[dict[str, object]]:
    records: list[dict[str, object]] = []
    for prompt, replies in curriculum:
        for offset in range(10):
            reply = replies[offset % len(replies)]
            records.append(_record(dialect, prompt, reply))
    return records


def _check(records: list[dict[str, object]]) -> None:
    for record in records:
        for msg in record["messages"]:  # type: ignore[index]
            text = str(msg["content"])  # type: ignore[index]
            bad = [term for term in FORBIDDEN if term in text]
            if bad:
                raise RuntimeError(f"forbidden operational term in dialogue text: {bad}")


def _write_jsonl(path: Path, records: list[dict[str, object]]) -> None:
    path.write_text(
        "\n".join(json.dumps(record, ensure_ascii=False) for record in records) + "\n",
        encoding="utf-8",
    )


def _write_card(path: Path, *, title: str, dialect: str, count: int) -> None:
    path.write_text(
        "\n".join(
            [
                f"# {title}",
                "",
                "- source: owner-delegated local authoring",
                f"- dialect: {dialect}",
                f"- records: {count}",
                "- quality: gold",
                "- training_allowed: true",
                "- synthetic_llm_data: false",
                "- external_dataset: false",
                "- purpose: social and lexical repair for natural everyday chat",
                "",
            ]
        ),
        encoding="utf-8",
    )


def main() -> int:
    msa = _build(MSA_CURRICULUM, "msa")
    saudi = _build(SAUDI_CURRICULUM, "saudi")
    _check(msa)
    _check(saudi)

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    msa_path = OUT_DIR / "dialogue_batch_v9_social_lexical_msa_009.jsonl"
    saudi_path = OUT_DIR / "dialogue_batch_v9_social_lexical_saudi_009.jsonl"
    _write_jsonl(msa_path, msa)
    _write_jsonl(saudi_path, saudi)
    _write_card(
        msa_path.with_suffix(".CARD.md"),
        title="Phase 27.15 Social Lexical Curriculum MSA 009",
        dialect="msa",
        count=len(msa),
    )
    _write_card(
        saudi_path.with_suffix(".CARD.md"),
        title="Phase 27.15 Social Lexical Curriculum Saudi 009",
        dialect="saudi",
        count=len(saudi),
    )

    print("SF.AI — Phase 27.15 social/lexical curriculum")
    print(f"  msa    : {msa_path} ({len(msa)})")
    print(f"  saudi  : {saudi_path} ({len(saudi)})")
    print(f"  total  : {len(msa) + len(saudi)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
