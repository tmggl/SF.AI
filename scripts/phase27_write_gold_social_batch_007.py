def _record(dialect: str, user: str, assistant: str) -> dict[str, object]:
    return {
        "domain": "chat",
        "lang": "ar",
        "messages": [
            {"role": "user", "content": user},
            {"role": "assistant", "content": assistant},
        ],
        "provenance": {
            "source": f"sf-ai-gold-social-dialogue-{dialect}-v1",
            "license": LICENSE,
            "language": "ar",
            "dialect": dialect,
            "quality": "gold",
            "training_allowed": True,
            "owner_user_id": OWNER_USER_ID,
            "created_by_user_id": CREATOR_USER_ID,
            "target_user_id": OWNER_USER_ID,
            "user_scope": "single_user",
            "notes": "gold everyday social dialogue; owner delegated local authoring; no external dataset",
        },
    }


def _check(records: list[dict[str, object]]) -> None:
    text = json.dumps(records, ensure_ascii=False)
    bad = [term for term in FORBIDDEN if term in text]
    if bad:
        raise RuntimeError(f"forbidden operational terms in gold batch: {bad}")


def _write_jsonl(path: Path, records: list[dict[str, object]]) -> None:
    path.write_text(
        "\n".join(json.dumps(record, ensure_ascii=False) for record in records) + "\n",
        encoding="utf-8",
    )


def _write_card(path: Path, *, title: str, dialect: str, count: int) -> None:
    path.write_text(
        "\n".join(
            [
                f"# {title}",
                "",
                "- source: owner-delegated local authoring",
                f"- dialect: {dialect}",
                f"- records: {count}",
                "- quality: gold",
                "- training_allowed: true",
                "- synthetic_llm_data: false",
                "- external_dataset: false",
                "- purpose: short everyday social replies for assistant-target training",
                "",
            ]
        ),
        encoding="utf-8",
    )


def main() -> int:
    msa = [_record("msa", user, assistant) for user, assistant in MSA_TURNS]
    saudi = [_record("saudi", user, assistant) for user, assistant in SAUDI_TURNS]
    if len(msa) != 50 or len(saudi) != 50:
        raise RuntimeError(f"unexpected counts: msa={len(msa)} saudi={len(saudi)}")
    _check(msa + saudi)
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    msa_path = OUT_DIR / "dialogue_batch_v7_gold_social_msa_007.jsonl"
    saudi_path = OUT_DIR / "dialogue_batch_v7_gold_social_saudi_007.jsonl"
    _write_jsonl(msa_path, msa)
    _write_jsonl(saudi_path, saudi)
    _write_card(msa_path.with_suffix(".CARD.md"), title=msa_path.name, dialect="msa", count=len(msa))
    _write_card(saudi_path.with_suffix(".CARD.md"), title=saudi_path.name, dialect="saudi", count=len(saudi))
    print(f"wrote {len(msa)} records: {msa_path}")
    print(f"wrote {len(saudi)} records: {saudi_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
