def _write_batch(
    *,
    filename: str,
    card: str,
    records: list[list[tuple[str, str]]],
    dialect: str,
    source: str,
    title: str,
    description: str,
) -> None:
    path = OUT_DIR / filename
    lines = [
        json.dumps(_record(turns, dialect=dialect, source=source), ensure_ascii=False)
        for turns in records
    ]
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    (OUT_DIR / card).write_text(_card(title, description, filename, source, dialect, len(records)), encoding="utf-8")


def _record(turns: list[tuple[str, str]], *, dialect: str, source: str) -> dict[str, object]:
    messages: list[dict[str, str]] = []
    for user, assistant in turns:
        messages.append({"role": "user", "content": user})
        messages.append({"role": "assistant", "content": assistant})
    return {
        "domain": "chat",
        "lang": "ar",
        "messages": messages,
        "provenance": {
            "source": source,
            "license": LICENSE,
            "language": "ar",
            "dialect": dialect,
            "quality": "silver",
            "training_allowed": True,
            "owner_user_id": USER_ID,
            "created_by_user_id": USER_ID,
            "target_user_id": USER_ID,
            "user_scope": "single_user",
            "notes": (
                "Owner delegated agent-side authoring, review, approval, and corpus insertion "
                "for SF.AI Phase 27 corpus expansion; transparent agent-authored dialogue; "
                "no external dataset, no pretrained model output, no raw SF-10M output."
            ),
        },
    }


