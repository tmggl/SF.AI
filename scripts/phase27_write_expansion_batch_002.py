    saudi_records = _build_records(
        dialect="saudi",
        source="phase27_batch_002_large_saudi_agent_authored",
        topics=SAUDI_TOPICS,
        frames=SAUDI_FRAMES,
    )

    msa_path = OUT_DIR / "dialogue_batch_v3_msa_002.jsonl"
    saudi_path = OUT_DIR / "dialogue_batch_v3_saudi_002.jsonl"
    _write_jsonl(msa_path, msa_records)
    _write_jsonl(saudi_path, saudi_records)
    _write_card(msa_path.with_suffix(".CARD.md"), title=msa_path.name, dialect="msa", count=len(msa_records))
    _write_card(saudi_path.with_suffix(".CARD.md"), title=saudi_path.name, dialect="saudi", count=len(saudi_records))
