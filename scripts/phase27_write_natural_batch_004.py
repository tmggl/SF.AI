def _record(dialect: str, messages: list[dict[str, str]]) -> dict[str, object]:
    return {
        "domain": "chat",
        "lang": "ar",
        "messages": messages,
        "provenance": {
            "source": f"sf-ai-natural-dialogue-{dialect}",
            "license": LICENSE,
            "language": "ar",
            "dialect": dialect,
            "quality": QUALITY,
            "training_allowed": True,
            "owner_user_id": OWNER_USER_ID,
            "created_by_user_id": CREATOR_USER_ID,
            "target_user_id": OWNER_USER_ID,
            "user_scope": "single_user",
            "notes": "general human dialogue; owner approved; no external dataset",
        },
    }


def _build_records(
    *,
    dialect: str,
    openings: tuple[str, ...],
    scenarios: tuple[tuple[str, str, str, str, str, str], ...],
) -> list[dict[str, object]]:
    records: list[dict[str, object]] = []
    for scenario_index, scenario in enumerate(scenarios):
        first_user, first_answer, second_user, second_answer, third_user, third_answer = scenario
        for opening_index, opening in enumerate(openings):
            if dialect == "msa":
                user1 = f"{opening} {first_user}"
                if opening_index % 5 == 0:
                    assistant1 = f"الأفضل أن تبدأ بهدوء: {first_answer}"
                elif opening_index % 5 == 1:
                    assistant1 = f"التصرف المناسب هو: {first_answer}"
                elif opening_index % 5 == 2:
                    assistant1 = f"ابدأ بهذه الفكرة: {first_answer}"
                elif opening_index % 5 == 3:
                    assistant1 = f"يمكنك التعامل معه ببساطة: {first_answer}"
                else:
                    assistant1 = f"الجواب العملي: {first_answer}"
                user2 = second_user
                assistant2 = second_answer
                user3 = third_user
                assistant3 = third_answer
            else:
                user1 = f"{opening} {first_user}"
                if opening_index % 5 == 0:
                    assistant1 = f"الأفضل تبدأ بهدوء: {first_answer}"
                elif opening_index % 5 == 1:
                    assistant1 = f"التصرف المناسب: {first_answer}"
                elif opening_index % 5 == 2:
                    assistant1 = f"ابدأ بهالفكرة: {first_answer}"
                elif opening_index % 5 == 3:
                    assistant1 = f"تعامل معها ببساطة: {first_answer}"
                else:
                    assistant1 = f"الزبدة: {first_answer}"
                user2 = second_user
                assistant2 = second_answer
                user3 = third_user
                assistant3 = third_answer

            if (scenario_index + opening_index) % 7 == 0:
                user2 = f"{user2} لو سمحت"
            if (scenario_index + opening_index) % 9 == 0:
                user3 = f"{user3} باختصار"

            messages = [
                {"role": "user", "content": user1.strip()},
                {"role": "assistant", "content": assistant1.strip()},
                {"role": "user", "content": user2.strip()},
                {"role": "assistant", "content": assistant2.strip()},
                {"role": "user", "content": user3.strip()},
                {"role": "assistant", "content": assistant3.strip()},
            ]
            joined = "\n".join(message["content"] for message in messages)
            found = [term for term in FORBIDDEN_SNIPPETS if term in joined]
            if found:
                raise ValueError(
                    f"forbidden snippets in {dialect} scenario {scenario_index} "
                    f"opening {opening_index}: {found}"
                )
            records.append(_record(dialect, messages))
    return records


def _write_jsonl(path: Path, records: list[dict[str, object]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        for record in records:
            handle.write(json.dumps(record, ensure_ascii=False, separators=(",", ":")) + "\n")


